

public class Foo {
    
    private String _content = null;
    private String _type = null;
    
    public Foo() {
        super();
    }
    
    public String getContent() {
        return _content;
    } //-- getContent
    
    public String getType() {
        return _type;
    } //-- getType
    
    public void setContent(String content) {
        _content = content;
    } //-- setContent

    
    public void setType(String type) {
        _type = type;
    } //-- setType
    
} //-- Foo